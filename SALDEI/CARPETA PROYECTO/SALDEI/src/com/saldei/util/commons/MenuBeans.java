package com.saldei.util.commons;

import java.util.Map;

public class MenuBeans {

	private String  id;
	private String  id_padre;
	private String  nombre;
	private String  descripcion;
	private String  url;
	private String  metodo; 
	private boolean Separador;
	private Integer orden;
	private Map     mapa;
	
	
	public MenuBeans()
	{}
	public MenuBeans(			
		String  id,
		String  id_padre,
		String  nombre,
		String  descripcion,
		String  url,
		String  metodo, 
		Integer orden, 
		boolean Separador
	)
	{
		setId(id);
		setIdPadre(id_padre);
		setNombre(nombre);
		setDescripcion(descripcion);
		setUrl(url);
		setMetodo(metodo);
		setOrden(orden);
		setSeparador(Separador);
	}	
	
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getId() {
		return id;
	}
	public String getIdPadre() {
		return id_padre;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setIdPadre(String id_padre) {
		this.id_padre = id_padre;
	}
	public Map getMapa() {
		return mapa;
	}
	public void setMapa(Map mapa) {
		this.mapa = mapa;
	}
	public String getMetodo() {
		return metodo;
	}
	public void setMetodo(String metodo) {
		this.metodo = metodo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Integer getOrden() {
		return orden;
	}
	public void setOrden(Integer orden) {
		this.orden = orden;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public boolean isSeparador() {
		return Separador;
	}
	public void setSeparador(boolean separador) {
		Separador = separador;
	}

}
