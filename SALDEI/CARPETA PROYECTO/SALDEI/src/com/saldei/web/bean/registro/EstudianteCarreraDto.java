package com.saldei.web.bean.registro;

public class EstudianteCarreraDto {
	
	private static final long serialVersionUID = 1L;
	private String carnetEst;
	private String idCarrera;
	private String planEstudio;
	private String fechaIni;
	private String fechaFin;
	private String estado;
	
	public String getCarnetEst() {
		return carnetEst;
	}
	public void setCarnetEst(String carnetEst) {
		this.carnetEst = carnetEst;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	public String getFechaIni() {
		return fechaIni;
	}
	public void setFechaIni(String fechaIni) {
		this.fechaIni = fechaIni;
	}
	public String getIdCarrera() {
		return idCarrera;
	}
	public void setIdCarrera(String idCarrera) {
		this.idCarrera = idCarrera;
	}
	public String getPlanEstudio() {
		return planEstudio;
	}
	public void setPlanEstudio(String planEstudio) {
		this.planEstudio = planEstudio;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
}
